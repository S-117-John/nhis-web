
package com.zebone.nhis.ma.pub.lb.vo.referral;


public class MainHealthIssues {

    private String inHospitalNo;
    private String sickbedNo;
    private String sickroomNo;
    private String sickbayName;
    private String admissionTime;
    private String admissionDesc;
    public void setInHospitalNo(String inHospitalNo) {
         this.inHospitalNo = inHospitalNo;
     }
     public String getInHospitalNo() {
         return inHospitalNo;
     }

    public void setSickbedNo(String sickbedNo) {
         this.sickbedNo = sickbedNo;
     }
     public String getSickbedNo() {
         return sickbedNo;
     }

    public void setSickroomNo(String sickroomNo) {
         this.sickroomNo = sickroomNo;
     }
     public String getSickroomNo() {
         return sickroomNo;
     }

    public void setSickbayName(String sickbayName) {
         this.sickbayName = sickbayName;
     }
     public String getSickbayName() {
         return sickbayName;
     }

    public void setAdmissionTime(String admissionTime) {
         this.admissionTime = admissionTime;
     }
     public String getAdmissionTime() {
         return admissionTime;
     }

    public void setAdmissionDesc(String admissionDesc) {
         this.admissionDesc = admissionDesc;
     }
     public String getAdmissionDesc() {
         return admissionDesc;
     }

}
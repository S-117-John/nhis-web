
package com.zebone.nhis.ma.pub.lb.vo.referral;


public class Operation {

    private String operationProcess;
    private String operationCode;
    private String operationName;
    private String operationTime;
    private String operationIncisionCode;
    private String woundHealingCode;
    private String anesthesiaMethodCode;
    private String anesthesiaMethodName;
    public void setOperationProcess(String operationProcess) {
         this.operationProcess = operationProcess;
     }
     public String getOperationProcess() {
         return operationProcess;
     }

    public void setOperationCode(String operationCode) {
         this.operationCode = operationCode;
     }
     public String getOperationCode() {
         return operationCode;
     }

    public void setOperationName(String operationName) {
         this.operationName = operationName;
     }
     public String getOperationName() {
         return operationName;
     }

    public void setOperationTime(String operationTime) {
         this.operationTime = operationTime;
     }
     public String getOperationTime() {
         return operationTime;
     }

    public void setOperationIncisionCode(String operationIncisionCode) {
         this.operationIncisionCode = operationIncisionCode;
     }
     public String getOperationIncisionCode() {
         return operationIncisionCode;
     }

    public void setWoundHealingCode(String woundHealingCode) {
         this.woundHealingCode = woundHealingCode;
     }
     public String getWoundHealingCode() {
         return woundHealingCode;
     }

    public void setAnesthesiaMethodCode(String anesthesiaMethodCode) {
         this.anesthesiaMethodCode = anesthesiaMethodCode;
     }
     public String getAnesthesiaMethodCode() {
         return anesthesiaMethodCode;
     }

    public void setAnesthesiaMethodName(String anesthesiaMethodName) {
         this.anesthesiaMethodName = anesthesiaMethodName;
     }
     public String getAnesthesiaMethodName() {
         return anesthesiaMethodName;
     }

}
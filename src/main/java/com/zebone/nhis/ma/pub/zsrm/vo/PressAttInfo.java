package com.zebone.nhis.ma.pub.zsrm.vo;

public class PressAttInfo {

    private String pkPres;

    private Double isQuan;

    private Integer attVal;

    private String attWin;

    public String getPkPres() {
        return pkPres;
    }

    public void setPkPres(String pkPres) {
        this.pkPres = pkPres;
    }

    public Double getIsQuan() {
        return isQuan;
    }

    public void setIsQuan(Double isQuan) {
        this.isQuan = isQuan;
    }

    public Integer getAttVal() {
        return attVal;
    }

    public void setAttVal(Integer attVal) {
        this.attVal = attVal;
    }

    public String getAttWin() {
        return attWin;
    }

    public void setAttWin(String attWin) {
        this.attWin = attWin;
    }
}
